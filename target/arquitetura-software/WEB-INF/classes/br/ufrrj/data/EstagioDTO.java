package org.example.data;

public class EstagioDTO {
    private String nome;
    private String matricula;
    private float IRA;
    private int cargaHoraria;
    private String enderecoDiscente;
    private boolean primeiroEstagio;
    private String nomeEmpresa;
    private String enderecoEmpresa;
    private ModalidadeEstagio modalidaEstagio;
    private int cargoHorariaSemanal;
    private String valorBolsa;
    private String resumoAtividades;
    private String relacaoDosConteudos;
    private String motivoDaIntencao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public float getIRA() {
        return IRA;
    }

    public void setIRA(float iRA) {
        IRA = iRA;
    }

    public int getCargaHoraria() {
        return cargaHoraria;
    }

    public void setCargaHoraria(int cargaHoraria) {
        this.cargaHoraria = cargaHoraria;
    }

    public String getEnderecoDiscente() {
        return enderecoDiscente;
    }

    public void setEnderecoDiscente(String enderecoDiscente) {
        this.enderecoDiscente = enderecoDiscente;
    }

    public boolean isPrimeiroEstagio() {
        return primeiroEstagio;
    }

    public void setPrimeiroEstagio(boolean primeiroEstagio) {
        this.primeiroEstagio = primeiroEstagio;
    }

    public String getNomeEmpresa() {
        return nomeEmpresa;
    }

    public void setNomeEmpresa(String nomeEmpresa) {
        this.nomeEmpresa = nomeEmpresa;
    }

    public String getEnderecoEmpresa() {
        return enderecoEmpresa;
    }

    public void setEnderecoEmpresa(String enderecoEmpresa) {
        this.enderecoEmpresa = enderecoEmpresa;
    }

    public ModalidadeEstagio getModalidadeEstagio() {
        return modalidaEstagio;
    }

    public void setModalidadeEstagio(ModalidadeEstagio modalidaEstagio) {
        this.modalidaEstagio = modalidaEstagio;
    }

    public int getCargaHorariaSemanal() {
        return cargoHorariaSemanal;
    }

    public void setCargaHorariaSemanal(int cargoHorariaSemanal) {
        this.cargoHorariaSemanal = cargoHorariaSemanal;
    }

    public String getValorBolsa() {
        return valorBolsa;
    }

    public void setValorBolsa(String valorBolsa) {
        this.valorBolsa = valorBolsa;
    }

    public String getResumoAtividades() {
        return resumoAtividades;
    }

    public void setResumoAtividades(String resumoAtividades) {
        this.resumoAtividades = resumoAtividades;
    }

    public String getRelacaoDosConteudos() {
        return relacaoDosConteudos;
    }

    public void setRelacaoDosConteudos(String relacaoDosConteudos) {
        this.relacaoDosConteudos = relacaoDosConteudos;
    }

    public String getMotivoDaIntencao() {
        return motivoDaIntencao;
    }

    public void setMotivoDaIntencao(String motivoDaIntencao) {
        this.motivoDaIntencao = motivoDaIntencao;
    }



}
